/**
 * @Author:pengfei
 * @Description
 * @Date:Created in 16:17 2018/7/17
 * @Modified by
 */
@EnableEurekaServer
@SpringBootApplication
public class EurekaServerApplication {

    public static void main(String[] args) {
        SpringApplication.run(EurekaServerApplication.class,args);
    }
}
